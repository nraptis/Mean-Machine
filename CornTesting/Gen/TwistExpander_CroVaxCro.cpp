void TwistExpander_CroVaxCro::Seed(TwistWorkSpace *pWorkspace,
                                 std::uint8_t *pSource,
                                 std::uint8_t *pPassword,
                                 unsigned int pPasswordByteLength) {
    TwistExpander::Seed(pWorkspace, pSource, pPassword, pPasswordByteLength);
    if (pWorkspace == nullptr) { return; }
    [[maybe_unused]] std::uint8_t *aExpandLaneA = pWorkspace->mExpandLaneA;
    [[maybe_unused]] std::uint8_t *aExpandLaneB = pWorkspace->mExpandLaneB;
    [[maybe_unused]] std::uint8_t *aExpandLaneC = pWorkspace->mExpandLaneC;
    [[maybe_unused]] std::uint8_t *aWorkLaneA = pWorkspace->mWorkLaneA;
    [[maybe_unused]] std::uint8_t *aWorkLaneB = pWorkspace->mWorkLaneB;
    [[maybe_unused]] std::uint8_t *aWorkLaneC = pWorkspace->mWorkLaneC;
    [[maybe_unused]] std::uint8_t *aWorkLaneD = pWorkspace->mWorkLaneD;
    std::size_t mSaltAKeyA = 0;
    std::uint64_t aValue = 0;
    std::size_t mSaltBKeyA = 0;
    std::size_t mSaltCKeyA = 0;
    std::uint64_t aValueMix = 0;
    std::size_t mSaltDKeyA = 0;
    std::uint8_t aUnrollByte = 0;

    //
    // Batch 1
    //

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 1U) {
        mSaltAKeyA = 127U - aIndex + 99U;
        aValue ^= TwistMix64::GateRoll_1_1(mSaltA[mSaltAKeyA & 0x7FU], 3ULL, mSBoxA);
        aValue += aExpandLaneA[aIndex];
        aWorkLaneA[aIndex] = aValue;
    }

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 1U) {
        mSaltBKeyA = 127U - aIndex + 99U;
        aValue ^= TwistMix64::GateRoll_1_1(mSaltB[mSaltBKeyA & 0x7FU], 3ULL, mSBoxA);
        aValue += pSource[aIndex] + aWorkLaneA[aIndex] + pSource[aIndex];
        aWorkLaneB[aIndex] = aValue;
    }

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 1U) {
        mSaltCKeyA = 127U - aIndex + 99U;
        aValueMix = (aWorkLaneA[aIndex] + mSaltC[mSaltCKeyA & 0x7FU]) ^ aWorkLaneB[aIndex];
        aValue ^= TwistMix64::GateRoll_1_1(aValueMix, 3ULL, mSBoxA);
        aValue += aWorkLaneA[aIndex];
        aWorkLaneC[aIndex] = aValue;
    }

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 1U) {
        mSaltDKeyA = 127U - aIndex + 99U;
        aValueMix = mSaltD[mSaltDKeyA & 0x7FU] ^ aWorkLaneC[aIndex];
        aValue ^= TwistMix64::GateRoll_1_1(aValueMix, 3ULL, mSBoxA);
        aValue += aWorkLaneB[aIndex];
        aWorkLaneD[aIndex] = aValue;
    }

    //
    // Batch 2
    //

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 64U) {
        mMatrixA.LoadAndReset(aWorkLaneA + aIndex);
        mMatrixA.TransposeAntiDiagonal(0U, 0U);
        mMatrixA.PinwheelRight(0U, 0U);
        mMatrixA.RotateLeft(0U, 0U);
        mMatrixA.TransposeMainDiagonalSixteenthsQuarterD(0U, 0U);
        mMatrixA.FlipVerticalEachSixteenthQuarterB(0U, 0U);
        mMatrixA.RotateRightSixteenthsQuarterB(0U, 0U);
        mMatrixA.TransposeAntiDiagonalEachSixteenthQuarterA(0U, 0U);
        mMatrixA.FlipHorizontalSixteenthsQuarterD(0U, 0U);
        mMatrixA.ShiftRing8D(0U, 0U);
        mMatrixA.FlipVerticalSixteenthsQuarterA(0U, 0U);
        mMatrixA.RotateLeftEachSixteenthQuarterA(0U, 0U);
        mMatrixA.ShiftRing24D(0U, 0U);
        mMatrixA.SwapRowsEven(0U, 0U);
        mMatrixA.RotateRow(0U, 0U);
        mMatrixA.ReverseRowEven(0U, 0U);
        mMatrixA.SwapColsOdd(0U, 0U);
        mMatrixA.ReverseCol(0U, 0U);
        mMatrixA.RotateCol(0U, 0U);
        mMatrixA.SwapSixteenths(0U, 0U);
        aUnrollByte = aWorkLaneB[aIndex + 5U];
        mMatrixA.Store(aWorkLaneA + aIndex, TwistFastMatrixUnrollScheme::kA, aUnrollByte);
    }

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 64U) {
        mMatrixA.LoadAndReset(aWorkLaneB + aIndex);
        mMatrixA.RotateLeftBlocks(0U, 0U);
        mMatrixA.RotateLeft(0U, 0U);
        mMatrixA.PinwheelLeft(0U, 0U);
        mMatrixA.TransposeAntiDiagonalBlocks(0U, 0U);
        mMatrixA.FlipVerticalSixteenthsQuarterD(0U, 0U);
        mMatrixA.TransposeMainDiagonalSixteenthsQuarterA(0U, 0U);
        mMatrixA.TransposeAntiDiagonalEachSixteenthQuarterC(0U, 0U);
        mMatrixA.FlipHorizontalEachSixteenthQuarterC(0U, 0U);
        mMatrixA.RotateRightEachSixteenthQuarterC(0U, 0U);
        mMatrixA.ShiftRing12B(0U, 0U);
        mMatrixA.TransposeMainDiagonalEachSixteenthQuarterA(0U, 0U);
        mMatrixA.TransposeAntiDiagonalSixteenthsQuarterC(0U, 0U);
        mMatrixA.RotateLeftSixteenthsQuarterD(0U, 0U);
        mMatrixA.ReverseRow(0U, 0U);
        mMatrixA.ReverseCol(0U, 0U);
        mMatrixA.RotateCol(0U, 0U);
        mMatrixA.RotateRow(0U, 0U);
        mMatrixA.SwapCols(0U, 0U);
        mMatrixA.SwapRowsOdd(0U, 0U);
        mMatrixA.SwapSixteenths(0U, 0U);
        aUnrollByte = aWorkLaneC[aIndex + 6U];
        mMatrixA.Store(aWorkLaneB + aIndex, TwistFastMatrixUnrollScheme::kC, aUnrollByte);
    }

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 64U) {
        mMatrixA.LoadAndReset(aWorkLaneC + aIndex);
        mMatrixA.RotateLeftBlocks(0U, 0U);
        mMatrixA.PinwheelLeft(0U, 0U);
        mMatrixA.FlipVertical(0U, 0U);
        mMatrixA.TransposeMainDiagonalBlocks(0U, 0U);
        mMatrixA.ShiftRing8D(0U, 0U);
        mMatrixA.RotateRightSixteenthsEachQuarter(0U, 0U);
        mMatrixA.TransposeAntiDiagonalEachSixteenthQuarterC(0U, 0U);
        mMatrixA.ShiftRing12E(0U, 0U);
        mMatrixA.TransposeAntiDiagonalSixteenthsQuarterC(0U, 0U);
        mMatrixA.FlipHorizontalEachSixteenthQuarterC(0U, 0U);
        mMatrixA.FlipVerticalEachSixteenthQuarterA(0U, 0U);
        mMatrixA.RotateLeftEachSixteenthQuarterD(0U, 0U);
        mMatrixA.FlipVerticalSixteenthsQuarterC(0U, 0U);
        mMatrixA.SwapRowsEven(0U, 0U);
        mMatrixA.RotateCol(0U, 0U);
        mMatrixA.RotateRow(0U, 0U);
        mMatrixA.SwapSixteenths(0U, 0U);
        mMatrixA.ReverseRowEven(0U, 0U);
        mMatrixA.ReverseCol(0U, 0U);
        mMatrixA.SwapColsOdd(0U, 0U);
        aUnrollByte = aWorkLaneD[aIndex + 4U];
        mMatrixA.Store(aWorkLaneC + aIndex, TwistFastMatrixUnrollScheme::kA, aUnrollByte);
    }

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 64U) {
        mMatrixA.LoadAndReset(aWorkLaneD + aIndex);
        mMatrixA.PinwheelRight(0U, 0U);
        mMatrixA.RotateRight(0U, 0U);
        mMatrixA.TransposeAntiDiagonalBlocks(0U, 0U);
        mMatrixA.TransposeMainDiagonal(0U, 0U);
        mMatrixA.FlipVerticalEachSixteenthQuarterC(0U, 0U);
        mMatrixA.ShiftRing20A(0U, 0U);
        mMatrixA.RotateLeftEachSixteenthQuarterA(0U, 0U);
        mMatrixA.RotateLeftSixteenthsQuarterA(0U, 0U);
        mMatrixA.RotateRightSixteenthsQuarterC(0U, 0U);
        mMatrixA.TransposeAntiDiagonalSixteenthsQuarterB(0U, 0U);
        mMatrixA.FlipHorizontalSixteenthsEachQuarter(0U, 0U);
        mMatrixA.TransposeMainDiagonalEachSixteenthQuarterD(0U, 0U);
        mMatrixA.RotateCol(0U, 0U);
        mMatrixA.RotateRow(0U, 0U);
        mMatrixA.SwapSixteenths(0U, 0U);
        mMatrixA.ReverseColEven(0U, 0U);
        mMatrixA.SwapRows(0U, 0U);
        mMatrixA.ReverseRow(0U, 0U);
        mMatrixA.SwapColsOdd(0U, 0U);
        aUnrollByte = aWorkLaneA[aIndex + 6U];
        mMatrixA.Store(aWorkLaneD + aIndex, TwistFastMatrixUnrollScheme::kC, aUnrollByte);
    }

    //
    // Batch 3
    //

    TwistMasking::MaskBraid(TwistMaskBraidType::kA, aExpandLaneA, aExpandLaneB, S_BLOCK, aExpandLaneC, S_BLOCK);

}

void TwistExpander_CroVaxCro::TwistBlock(TwistWorkSpace *pWorkspace,
                                       std::uint8_t *pSource,
                                       std::uint8_t *pDestination) {
    TwistExpander::TwistBlock(pWorkspace, pSource, pDestination);
    if ((pWorkspace == nullptr) || (pDestination == nullptr)) { return; }
    std::memcpy(pDestination, pWorkspace->mWorkLaneD, S_BLOCK);
}
