void TwistExpander_Remington::Seed(TwistWorkSpace *pWorkspace,
                                 std::uint8_t *pSource,
                                 std::uint8_t *pPassword,
                                 unsigned int pPasswordByteLength) {
    TwistExpander::Seed(pWorkspace, pSource, pPassword, pPasswordByteLength);
    if (pWorkspace == nullptr) { return; }
    [[maybe_unused]] std::uint8_t *aExpandLaneA = pWorkspace->mExpandLaneA;
    [[maybe_unused]] std::uint8_t *aExpandLaneB = pWorkspace->mExpandLaneB;
    [[maybe_unused]] std::uint8_t *aWorkLaneA = pWorkspace->mWorkLaneA;
    [[maybe_unused]] std::uint8_t *aWorkLaneB = pWorkspace->mWorkLaneB;
    [[maybe_unused]] std::uint8_t *aWorkLaneC = pWorkspace->mWorkLaneC;
    [[maybe_unused]] std::uint8_t *aWorkLaneD = pWorkspace->mWorkLaneD;
    std::uint64_t aValuePrevious = 0;
    std::uint64_t e = 0;
    std::uint64_t sec_a = 0;
    std::uint64_t sec_b = 0;
    std::uint64_t cross = 0;
    std::uint64_t a = 0;
    std::uint64_t s0 = 0;
    std::uint64_t b = 0;
    std::uint64_t s1 = 0;
    std::uint64_t c = 0;
    std::uint64_t s2 = 0;
    std::uint64_t d = 0;
    std::uint64_t carry = 0;
    std::uint64_t aValueMix = 0;
    std::uint64_t aValue = 0;
    std::uint64_t aSelect = 0;

    // Making snow counter: aes 256

    TwistSnow::AES256Counter(pSource, aExpandLaneA);
    TwistSnow::AES256Counter(aExpandLaneA, aExpandLaneB);

    TwistMemory::Grow(mScratchSaltD, mSaltA, 128U);
    TwistMemory::Grow(mScratchSaltA, mSaltD, 128U);
    TwistMemory::Grow(mScratchSaltC, mSaltB, 128U);
    TwistMemory::Grow(mScratchSaltB, mSaltC, 128U);

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 1U) {
        aValuePrevious = e;
        sec_a = aExpandLaneB[aIndex];
        sec_b = aExpandLaneA[S_BLOCK1 - aIndex];
        cross = aExpandLaneA[aIndex];
        e = sec_b;
        // __START_CSPRNG
        // a = s0 + e + hot0.add;
        a = s0 + RotL64(e, 5U) + 12191915750254584983ULL;
        // b = s1 ^ previous ^ hot1.add;
        b = s1 ^ RotL64(aValuePrevious, 35U) ^ 131582210200048615ULL;
        // c = s2 + cross + fixedSaltA + scratchSaltA;
        c = s2 + RotL64(cross, 19U) + mSaltA[((127U - aIndex + 23U) & 0x7FU)] + mScratchSaltD[((aIndex + 12U) & 0x7FU)];
        // d = carry ^ fixedSaltB ^ scratchSaltB ^ RotL64(e, r0);
        d = RotL64(carry, 43U) ^ RotL64(e, 21U) ^ mSaltD[((127U - aIndex + 101U) & 0x7FU)] ^ mScratchSaltA[((aIndex + 94U) & 0x7FU)];
        // a += b;
        // d ^= a;
        // d = RotL64(d, r1);
        a = a + b;
        d = d ^ a ^ mScratchSaltB[(((carry ^ sec_a) + 4U) & 0x7FU)];
        d = RotL64(d, 35U);
        // c += d;
        // b ^= c;
        // b = RotL64(b, r2);
        c = c + d + mScratchSaltB[(((cross ^ sec_a) + 54U) & 0x7FU)];
        b = b ^ c ^ 3054500413081257597ULL;
        b = RotL64(b, 27U);
        // a += b + hot4.add;
        // d ^= a;
        // d *= hot4.mul;
        // d = RotL64(d, r3);
        a = a + b + 15178064206279447263ULL;
        d = d ^ a;
        d = d * 18012283899262758641ULL;
        d = RotL64(d, 51U);
        // c += d + hot4.add;
        // b ^= c;
        // b *= hot4.mul;
        // b = RotL64(b, r4);
        c = c + d + 16049149428947630314ULL;
        b = b ^ c;
        b = b * 13702664255233372033ULL;
        b = RotL64(b, 53U);
        // Chain up "p" main combine expression
        e = (RotL64(a, 11U) ^ b) + (RotL64(c, 19U) ^ d);
        aValueMix = TwistMix64::Diffuse(ShiftL64(sec_a, 32U) ^ sec_b ^ RotL64(aValuePrevious, 21U));
        // Push "p" through secure byte diffusion word
        e = e ^ aValueMix;
        e = TwistMix64::Diffuse(e);
        aWorkLaneA[aIndex] = e;
        // Pass "p" through prismatic gate
        aValue = e;
        aValue = aValue ^ RotL64(s1, 43U);
        aValue = aValue ^ a ^ RotL64(b, 27U);
        aValue = TwistMix64::Diffuse(aValue);
        aValue = aValue ^ RotL64(sec_a, 7U);
        aValue = TwistMix64::Diffuse(aValue);
        aSelect = aValue;
        if ((aSelect & 0x6AU) > 47) {
        	e = TwistMix64::GateRollB_4_8(e, 51ULL, mSBoxB, mSBoxA, mSBoxC, mSBoxD);
        } else {
        	e = TwistMix64::GateRollC_4_8(e, 53ULL, mSBoxB, mSBoxA, mSBoxC, mSBoxD);
        }
        // s0 += e ^ previous ^ d;
        // s0 *= hot0.mul;
        // s0 = RotL64(s0, r3);
        s0 = s0 + (e ^ aValuePrevious ^ d);
        s0 = s0 + mSaltD[(((aValuePrevious ^ cross) + 110U) & 0x7FU)];
        s0 = s0 * 10497303007298788949ULL;
        s0 = RotL64(s0, 35U);
        // s1 ^= e + cross + a;
        // s1 *= hot1.mul;
        // s1 = RotL64(s1, r4);
        s1 = s1 ^ (e + cross + a) ^ mScratchSaltC[(((a ^ cross) + 10U) & 0x7FU)];
        s1 = s1 * 5810598635849069251ULL;
        s1 = RotL64(s1, 51U);
        // s2 += b ^ c ^ fixedSalt;
        // s2 *= hot2.mul;
        // s2 = RotL64(s2, r5);
        s2 = s2 + (b ^ c ^ mSaltA[((aIndex + 5U) & 0x7FU)]);
        s2 = s2 * 4721763854098020963ULL;
        s2 = RotL64(s2, 51U);
        // carry += s0 ^ s1 ^ s2 ^ e;
        // carry *= hot7.mul;
        // carry = RotL64(carry, r6);
        // carry ^= carry >> rshift;
        carry = carry + (s0 ^ s1 ^ s2 ^ e);
        carry = carry * 4057546188715483405ULL;
        carry = RotL64(carry, 53U);
        carry = carry ^ ShiftR64(carry, 37U);
        // __END_CSPRNG
    }

    // Secure zero on crypto buffer.

    TwistMemory::ZeroBlock(aExpandLaneA);
    TwistMemory::ZeroBlock(aExpandLaneB);

    // Making snow counter: sha 256

    TwistSnow::Sha256Counter(pSource, aExpandLaneA);
    TwistSnow::Sha256Counter(aExpandLaneA, aExpandLaneB);

    TwistMemory::Grow(mScratchSaltC, mSaltD, 128U);
    TwistMemory::Grow(mScratchSaltB, mSaltB, 128U);
    TwistMemory::Grow(mScratchSaltD, mSaltC, 128U);
    TwistMemory::Grow(mScratchSaltA, mSaltA, 128U);

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 1U) {
        aValuePrevious = e;
        sec_a = aExpandLaneB[S_BLOCK1 - aIndex];
        sec_b = aExpandLaneA[S_BLOCK1 - aIndex];
        cross = aExpandLaneB[aIndex];
        e = sec_a;
        // __START_CSPRNG
        // a = s0 + e + hot0.add;
        a = s0 + RotL64(e, 1U) + 4714254121773480761ULL;
        // b = s1 ^ previous ^ hot1.add;
        b = s1 ^ RotL64(aValuePrevious, 45U) ^ 12719774865051505415ULL;
        // c = s2 + cross + fixedSaltA + scratchSaltA;
        c = s2 + RotL64(cross, 51U) + mSaltD[((aIndex + 38U) & 0x7FU)] + mScratchSaltC[((127U - aIndex + 28U) & 0x7FU)];
        // d = carry ^ fixedSaltB ^ scratchSaltB ^ RotL64(e, r0);
        d = RotL64(carry, 53U) ^ RotL64(e, 29U) ^ mSaltB[((127U - aIndex + 71U) & 0x7FU)] ^ mScratchSaltB[((aIndex + 81U) & 0x7FU)];
        // a += b;
        // d ^= a;
        // d = RotL64(d, r1);
        a = a + b + mScratchSaltA[(((carry ^ sec_a) + 119U) & 0x7FU)] + 16041310825161308530ULL;
        d = d ^ a;
        d = RotL64(d, 27U);
        // c += d;
        // b ^= c;
        // b = RotL64(b, r2);
        c = c + d + 17983621439010993981ULL;
        b = b ^ c ^ mScratchSaltA[(((cross ^ sec_b) + 89U) & 0x7FU)];
        b = RotL64(b, 11U);
        // a += b + hot4.add;
        // d ^= a;
        // d *= hot4.mul;
        // d = RotL64(d, r3);
        a = a + b + 9877214802443864835ULL;
        d = d ^ a;
        d = d * 14956561894254835193ULL;
        d = RotL64(d, 19U);
        // c += d + hot4.add;
        // b ^= c;
        // b *= hot4.mul;
        // b = RotL64(b, r4);
        c = c + d + 10554263146660944689ULL;
        b = b ^ c;
        b = b * 4301372799565802491ULL;
        b = RotL64(b, 13U);
        // Chain up "p" main combine expression
        e = (a ^ RotL64(b, 21U)) + (c ^ RotL64(d, 27U));
        aValueMix = TwistMix64::Diffuse(ShiftL64(sec_a, 32U) ^ sec_b ^ RotL64(aValuePrevious, 11U));
        // Push "p" through secure byte diffusion word
        e = e + aValueMix;
        e = TwistMix64::Diffuse(e);
        aWorkLaneB[S_BLOCK1 - aIndex] = e;
        // Pass "p" through prismatic gate
        aValue = e;
        aValue = aValue ^ RotL64(s0, 43U);
        aValue = aValue ^ RotL64(d, 19U) ^ b;
        aValue = TwistMix64::Diffuse(aValue);
        aValue = aValue + RotL64(sec_b, 21U);
        aValue = TwistMix64::Diffuse(aValue);
        aSelect = aValue;
        if ((aSelect & 0x1EU) > 15) {
        	e = TwistMix64::GateTurnC_4_8(e, 37ULL, mSBoxD, mSBoxB, mSBoxA, mSBoxC);
        } else {
        	e = TwistMix64::GateTurnA_4_8(e, 45ULL, mSBoxD, mSBoxB, mSBoxA, mSBoxC);
        }
        // s0 += e ^ previous ^ d;
        // s0 *= hot0.mul;
        // s0 = RotL64(s0, r3);
        s0 = s0 + (e ^ aValuePrevious ^ d);
        s0 = s0 + mSaltD[((cross + sec_a + 29U) & 0x7FU)];
        s0 = s0 * 7013418997134973357ULL;
        s0 = RotL64(s0, 27U);
        // s1 ^= e + cross + a;
        // s1 *= hot1.mul;
        // s1 = RotL64(s1, r4);
        s1 = s1 ^ (e + cross + a) ^ mScratchSaltD[(((e ^ sec_b) + 34U) & 0x7FU)];
        s1 = s1 * 4439612937210029533ULL;
        s1 = RotL64(s1, 35U);
        // s2 += b ^ c ^ fixedSalt;
        // s2 *= hot2.mul;
        // s2 = RotL64(s2, r5);
        s2 = s2 + (b ^ c ^ mSaltB[((127U - aIndex + 53U) & 0x7FU)]);
        s2 = s2 * 1092826363616991607ULL;
        s2 = RotL64(s2, 37U);
        // carry += s0 ^ s1 ^ s2 ^ e;
        // carry *= hot7.mul;
        // carry = RotL64(carry, r6);
        // carry ^= carry >> rshift;
        carry = carry + (s0 ^ s1 ^ s2 ^ e);
        carry = carry * 334659368971890877ULL;
        carry = RotL64(carry, 51U);
        carry = carry ^ ShiftR64(carry, 27U);
        // __END_CSPRNG
    }

    // Secure zero on crypto buffer.

    TwistMemory::ZeroBlock(aExpandLaneA);
    TwistMemory::ZeroBlock(aExpandLaneB);

    // Making snow counter: aria 256

    TwistSnow::Aria256Counter(pSource, aExpandLaneA);
    TwistSnow::Aria256Counter(aExpandLaneA, aExpandLaneB);

    TwistMemory::Grow(mScratchSaltB, mSaltB, 128U);
    TwistMemory::Grow(mScratchSaltD, mSaltC, 128U);
    TwistMemory::Grow(mScratchSaltA, mSaltA, 128U);
    TwistMemory::Grow(mScratchSaltC, mSaltD, 128U);

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 1U) {
        aValuePrevious = e;
        sec_a = aExpandLaneA[S_BLOCK1 - aIndex];
        sec_b = aExpandLaneB[aIndex];
        cross = aExpandLaneB[S_BLOCK1 - aIndex];
        e = sec_b;
        // __START_CSPRNG
        // a = s0 + e + hot0.add;
        a = s0 + RotL64(e, 13U) + 4581456012075548581ULL;
        // b = s1 ^ previous ^ hot1.add;
        b = s1 ^ RotL64(aValuePrevious, 45U) ^ 7092756233861150478ULL;
        // c = s2 + cross + fixedSaltA + scratchSaltA;
        c = s2 + RotL64(cross, 53U) + mSaltB[((127U - aIndex + 110U) & 0x7FU)] + mScratchSaltB[((aIndex + 6U) & 0x7FU)];
        // d = carry ^ fixedSaltB ^ scratchSaltB ^ RotL64(e, r0);
        d = RotL64(carry, 11U) ^ RotL64(e, 45U) ^ mSaltC[((aIndex + 42U) & 0x7FU)] ^ mScratchSaltD[((127U - aIndex + 110U) & 0x7FU)];
        // a += b;
        // d ^= a;
        // d = RotL64(d, r1);
        a = a + b + mScratchSaltD[((aValuePrevious + sec_b + 65U) & 0x7FU)];
        d = d ^ a ^ 4896071878102567566ULL;
        d = RotL64(d, 19U);
        // c += d;
        // b ^= c;
        // b = RotL64(b, r2);
        c = c + d;
        b = b ^ c ^ mScratchSaltD[(((cross ^ sec_a) + 77U) & 0x7FU)] ^ 5405920689605118006ULL;
        b = RotL64(b, 43U);
        // a += b + hot4.add;
        // d ^= a;
        // d *= hot4.mul;
        // d = RotL64(d, r3);
        a = a + b + 7068437165967667586ULL;
        d = d ^ a;
        d = d * 7471158782583781535ULL;
        d = RotL64(d, 35U);
        // c += d + hot4.add;
        // b ^= c;
        // b *= hot4.mul;
        // b = RotL64(b, r4);
        c = c + d + 18271038308613319037ULL;
        b = b ^ c;
        b = b * 3412318732803378973ULL;
        b = RotL64(b, 37U);
        // Chain up "p" main combine expression
        e = (a ^ RotL64(d, 21U)) + (b ^ RotL64(c, 29U));
        aValueMix = TwistMix64::Diffuse(sec_a ^ ShiftL64(sec_b, 32U) ^ RotL64(aValuePrevious, 11U));
        // Push "p" through secure byte diffusion word
        e = e + aValueMix;
        e = TwistMix64::Diffuse(e);
        aWorkLaneC[aIndex] = e;
        // Pass "p" through prismatic gate
        aValue = e;
        aValue = aValue ^ RotL64(s1, 53U);
        aValue = aValue ^ RotL64(c, 19U) ^ b;
        aValue = TwistMix64::Diffuse(aValue);
        aValue = aValue + RotL64(sec_a, 21U);
        aValue = TwistMix64::Diffuse(aValue);
        aSelect = aValue;
        if ((aSelect & 0x4CU) > 49) {
        	e = TwistMix64::GateRollA_4_8(e, 51ULL, mSBoxC, mSBoxD, mSBoxB, mSBoxA);
        } else {
        	e = TwistMix64::GateRollB_4_8(e, 13ULL, mSBoxC, mSBoxD, mSBoxB, mSBoxA);
        }
        // s0 += e ^ previous ^ d;
        // s0 *= hot0.mul;
        // s0 = RotL64(s0, r3);
        s0 = s0 + (e ^ aValuePrevious ^ d);
        s0 = s0 + mSaltC[((cross + sec_a + 107U) & 0x7FU)];
        s0 = s0 * 1460876498030652875ULL;
        s0 = RotL64(s0, 51U);
        // s1 ^= e + cross + a;
        // s1 *= hot1.mul;
        // s1 = RotL64(s1, r4);
        s1 = s1 ^ (e + cross + a) ^ mScratchSaltC[((cross + sec_a + 86U) & 0x7FU)];
        s1 = s1 * 16738016723078405111ULL;
        s1 = RotL64(s1, 43U);
        // s2 += b ^ c ^ fixedSalt;
        // s2 *= hot2.mul;
        // s2 = RotL64(s2, r5);
        s2 = s2 + (b ^ c ^ mSaltB[((127U - aIndex + 47U) & 0x7FU)]);
        s2 = s2 * 8341919074424203667ULL;
        s2 = RotL64(s2, 35U);
        // carry += s0 ^ s1 ^ s2 ^ e;
        // carry *= hot7.mul;
        // carry = RotL64(carry, r6);
        // carry ^= carry >> rshift;
        carry = carry + (s0 ^ s1 ^ s2 ^ e);
        carry = carry * 15621892292900496015ULL;
        carry = RotL64(carry, 13U);
        carry = carry ^ ShiftR64(carry, 31U);
        // __END_CSPRNG
    }

    // Secure zero on crypto buffer.

    TwistMemory::ZeroBlock(aExpandLaneA);
    TwistMemory::ZeroBlock(aExpandLaneB);

    // Making snow counter: cha cha 20

    TwistSnow::ChaCha20Counter(pSource, aExpandLaneA);
    TwistSnow::ChaCha20Counter(aExpandLaneA, aExpandLaneB);

    TwistMemory::Grow(mScratchSaltA, mSaltC, 128U);
    TwistMemory::Grow(mScratchSaltC, mSaltA, 128U);
    TwistMemory::Grow(mScratchSaltB, mSaltD, 128U);
    TwistMemory::Grow(mScratchSaltD, mSaltB, 128U);

    for (std::uint16_t aIndex = 0U; aIndex < S_BLOCK; aIndex += 1U) {
        aValuePrevious = e;
        sec_a = aExpandLaneA[S_BLOCK1 - aIndex];
        sec_b = aExpandLaneB[S_BLOCK1 - aIndex];
        cross = aExpandLaneA[aIndex];
        e = sec_a;
        // __START_CSPRNG
        // a = s0 + e + hot0.add;
        a = s0 + RotL64(e, 29U) + 7264055884651018793ULL;
        // b = s1 ^ previous ^ hot1.add;
        b = s1 ^ RotL64(aValuePrevious, 37U) ^ 2950228849917044260ULL;
        // c = s2 + cross + fixedSaltA + scratchSaltA;
        c = s2 + RotL64(cross, 45U) + mSaltC[((127U - aIndex + 70U) & 0x7FU)] + mScratchSaltA[((aIndex + 48U) & 0x7FU)];
        // d = carry ^ fixedSaltB ^ scratchSaltB ^ RotL64(e, r0);
        d = RotL64(carry, 27U) ^ RotL64(e, 21U) ^ mSaltA[((aIndex + 1U) & 0x7FU)] ^ mScratchSaltC[((127U - aIndex + 103U) & 0x7FU)];
        // a += b;
        // d ^= a;
        // d = RotL64(d, r1);
        a = a + b + mScratchSaltA[((carry + sec_a + 6U) & 0x7FU)];
        d = d ^ a ^ 13397864124185702106ULL;
        d = RotL64(d, 13U);
        // c += d;
        // b ^= c;
        // b = RotL64(b, r2);
        c = c + d + 6995667591673001213ULL;
        b = b ^ c ^ mScratchSaltA[(((aValue ^ sec_b) + 12U) & 0x7FU)];
        b = RotL64(b, 11U);
        // a += b + hot4.add;
        // d ^= a;
        // d *= hot4.mul;
        // d = RotL64(d, r3);
        a = a + b + 3360240144980893918ULL;
        d = d ^ a;
        d = d * 14938148063424955233ULL;
        d = RotL64(d, 37U);
        // c += d + hot4.add;
        // b ^= c;
        // b *= hot4.mul;
        // b = RotL64(b, r4);
        c = c + d + 8054982038916672949ULL;
        b = b ^ c;
        b = b * 17772899028324819669ULL;
        b = RotL64(b, 29U);
        // Chain up "p" main combine expression
        e = (RotL64(a, 13U) ^ b) + (c ^ RotL64(d, 29U));
        aValueMix = TwistMix64::Diffuse(sec_a ^ ShiftL64(sec_b, 32U) ^ RotL64(aValuePrevious, 13U));
        // Push "p" through secure byte diffusion word
        e = e + aValueMix;
        e = TwistMix64::Diffuse(e);
        aWorkLaneD[S_BLOCK1 - aIndex] = e;
        // Pass "p" through prismatic gate
        aValue = e;
        aValue = aValue ^ RotL64(s1, 45U);
        aValue = aValue ^ a ^ RotL64(c, 29U);
        aValue = TwistMix64::Diffuse(aValue);
        aValue = aValue ^ RotL64(sec_b, 13U);
        aValue = TwistMix64::Diffuse(aValue);
        aSelect = aValue;
        if ((aSelect & 0x16U) > 9) {
        	e = TwistMix64::GateRollC_4_8(e, 43ULL, mSBoxA, mSBoxC, mSBoxD, mSBoxB);
        } else {
        	e = TwistMix64::GateTurnB_4_8(e, 21ULL, mSBoxA, mSBoxC, mSBoxD, mSBoxB);
        }
        // s0 += e ^ previous ^ d;
        // s0 *= hot0.mul;
        // s0 = RotL64(s0, r3);
        s0 = s0 + (e ^ aValuePrevious ^ d);
        s0 = s0 + mSaltA[((aValuePrevious + cross + 66U) & 0x7FU)];
        s0 = s0 * 7720829276422159377ULL;
        s0 = RotL64(s0, 37U);
        // s1 ^= e + cross + a;
        // s1 *= hot1.mul;
        // s1 = RotL64(s1, r4);
        s1 = s1 ^ (e + cross + a) ^ mScratchSaltB[((cross + sec_b + 69U) & 0x7FU)];
        s1 = s1 * 7090949039097081517ULL;
        s1 = RotL64(s1, 45U);
        // s2 += b ^ c ^ fixedSalt;
        // s2 *= hot2.mul;
        // s2 = RotL64(s2, r5);
        s2 = s2 + (b ^ c ^ mSaltA[((127U - aIndex + 12U) & 0x7FU)]);
        s2 = s2 * 5392141865415645607ULL;
        s2 = RotL64(s2, 37U);
        // carry += s0 ^ s1 ^ s2 ^ e;
        // carry *= hot7.mul;
        // carry = RotL64(carry, r6);
        // carry ^= carry >> rshift;
        carry = carry + (s0 ^ s1 ^ s2 ^ e);
        carry = carry * 4317237461525766693ULL;
        carry = RotL64(carry, 45U);
        carry = carry ^ ShiftR64(carry, 41U);
        // __END_CSPRNG
    }

    // Secure zero on crypto buffer.

    TwistMemory::ZeroBlock(aExpandLaneA);
    TwistMemory::ZeroBlock(aExpandLaneB);

    // Secure Zero Out

    TwistMemory::ZeroBlock(aExpandLaneA);

    // Secure Zero Out

    TwistMemory::ZeroBlock(aExpandLaneB);

}

void TwistExpander_Remington::TwistBlock(TwistWorkSpace *pWorkspace,
                                       std::uint8_t *pSource,
                                       std::uint8_t *pDestination) {
    TwistExpander::TwistBlock(pWorkspace, pSource, pDestination);
    if ((pWorkspace == nullptr) || (pDestination == nullptr)) { return; }
    std::memcpy(pDestination, pWorkspace->mWorkLaneD, S_BLOCK);
}
